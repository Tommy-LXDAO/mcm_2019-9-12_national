function ansi=m_input(m1, T, P2, t)%0.01毫秒
t = mod(t, T);%通过确认工作状态获得高度，进而获得体积
st = t/T*6.28;
H = 4.826-2.412*cos(1.001*st)-0.006689*sin(1.001*st);
V1 = pi*6.25*H+20;%

p1 = m1 / V1;
P1 = (19841868198706151424*tanh((403683841933312*log(p1))/(134389394540145*log(3060513257434037/1125899906842624)) - 73773222113312768/83993371587590625))/6605507520437207 + 2737;
if(P1>P2)
    ansi = 0.85.*pi.*(0.7.^2).*sqrt(2.*(P1-P2)*p1)/264;%输出结果为一毫秒进油质量(mg/ms)
else
    ansi = 0;
end
